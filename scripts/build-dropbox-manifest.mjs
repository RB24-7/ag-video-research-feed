#!/usr/bin/env node

import { mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';

const DEFAULT_OUTPUT = 'data/video-manifest.json';
const EXPECTED_MODEL_ORDER = ['anythingv5', 'revanimated', 'toonyou', 'dreamshaper', 'sd15'];
const MODEL_LABELS = {
  anythingv5: { modelName: 'Anything V5', shortLabel: 'Anything' },
  reanimated: { modelName: 'ReV Animated', shortLabel: 'ReV' },
  revanimated: { modelName: 'ReV Animated', shortLabel: 'ReV' },
  toonyou: { modelName: 'ToonYou', shortLabel: 'Toon' },
  dreamshaper: { modelName: 'DreamShaper', shortLabel: 'Dream' },
  sd15: { modelName: 'SD 1.5', shortLabel: 'SD15' }
};
const STOPWORDS = new Set([
  'and', 'the', 'for', 'with', 'from', 'this', 'that', 'video', 'ad', 'commercial',
  'ads', 'template', 'editable', 'advertising', 'campaign', 'weekly', 'animation',
  'animated', 'about', 'all', 'your', 'you', 'it', 'its', 'order', 'now', 'yours',
  'days', 'pack'
]);
const CSV_HEADER_VALUES = new Set([
  'frame', 'frame_id', 'frameid', 'timestamp', 'time', 'start', 'end', 'confidence',
  'score', 'source', 'rank', 'count', 'frequency', 'keyword', 'keywords', 'label',
  'labels', 'object', 'objects', 'class', 'classes', 'term', 'terms', 'concept',
  'concepts', 'caption', 'description', 'video', 'file', 'filename', 'model'
]);
const KEYWORD_COLUMN_NAMES = new Set([
  'keyword', 'keywords', 'label', 'labels', 'object', 'objects', 'class', 'classes',
  'term', 'terms', 'concept', 'concepts'
]);

const args = parseArgs(process.argv.slice(2));
await loadDotEnv(args.env || '.env');
const outputPath = args.out || env('MANIFEST_OUTPUT') || DEFAULT_OUTPUT;
const originalsFolder = args.originals || env('DROPBOX_ORIGINAL_ADS_FOLDER');
const generatedFolder = args.generated || env('DROPBOX_GENERATED_OUTPUTS_FOLDER');
const accessToken = await getAccessToken();

if (!originalsFolder) {
  fail('Missing DROPBOX_ORIGINAL_ADS_FOLDER or --originals.');
}
if (!generatedFolder) {
  fail('Missing DROPBOX_GENERATED_OUTPUTS_FOLDER or --generated.');
}
if (!accessToken) {
  fail('Missing Dropbox credentials. Set DROPBOX_ACCESS_TOKEN, or set DROPBOX_REFRESH_TOKEN with DROPBOX_APP_KEY and DROPBOX_APP_SECRET.');
}

const originalRoot = makeDropboxRoot(originalsFolder);
const generatedRoot = makeDropboxRoot(generatedFolder);

const debug = args.debug === 'true' || args.verbose === 'true';

const originalFiles = (await listDropboxFolder(originalRoot))
  .filter((entry) => entry['.tag'] === 'file' && isVideoFile(entry.name))
  .sort((left, right) => left.name.localeCompare(right.name));
const generatedFolders = (await discoverGeneratedFolders(generatedRoot))
  .sort((left, right) => left.name.localeCompare(right.name));
const generatedBySetId = new Map();

for (const folder of generatedFolders) {
  const setId = slugId(folder.name);
  const folderRef = folder.ref || childDropboxRef(generatedRoot, folder.name, folder);
  const videoFiles = folder.entries
    .filter((entry) => entry['.tag'] === 'file' && isGeneratedVideoFile(entry.name))
    .sort((left, right) => modelSortKey(left.name) - modelSortKey(right.name));
  const keywords = await readKeywords(folder.entries, folderRef);

  generatedBySetId.set(setId, {
    setId,
    title: titleFromSlug(setId),
    outputFolder: folderRef.shareUrl || '',
    keywords,
    variants: await Promise.all(videoFiles.map(async (file, index) => {
      const modelKey = modelKeyFromFilename(file.name);
      const labels = MODEL_LABELS[modelKey] || {
        modelName: titleFromSlug(modelKey || `variant-${index + 1}`),
        shortLabel: `V${index + 1}`
      };

      return {
        modelName: labels.modelName,
        shortLabel: labels.shortLabel,
        fileName: file.name,
        url: await getOrCreateSharedLink(file)
      };
    }))
  });
}

const matchedGeneratedIds = new Set();
const sets = await Promise.all(originalFiles.map(async (file) => {
  const setId = slugId(stripExtension(file.name));
  const generated = findGeneratedMatch(setId, generatedBySetId);
  if (generated) matchedGeneratedIds.add(generated.setId);
  const keywords = uniqueList([
    ...(generated?.keywords || []),
    ...keywordsFromTitle(stripExtension(file.name))
  ]).slice(0, 8);

  return {
    id: setId,
    title: titleFromSlug(setId),
    keywords,
    tags: ['dropbox', 'source-ad'],
    outputFolder: generated?.outputFolder || '',
    seed: {
      title: titleFromSlug(setId),
      fileName: file.name,
      url: await getOrCreateSharedLink(file)
    },
    variants: generated?.variants || []
  };
}));

const manifest = {
  generatedAt: new Date().toISOString(),
  source: {
    originalsFolder: redactToken(originalsFolder),
    generatedOutputsFolder: redactToken(generatedFolder)
  },
  instructions: 'Generated by scripts/build-dropbox-manifest.mjs. Do not edit by hand; update Dropbox and rerun the builder.',
  sets,
  videos: []
};

await mkdir(path.dirname(outputPath), { recursive: true });
await writeFile(outputPath, `${JSON.stringify(manifest, null, 2)}\n`);

const discoveredVariantCount = [...generatedBySetId.values()]
  .reduce((total, set) => total + set.variants.length, 0);
const variantCount = sets.reduce((total, set) => total + set.variants.length, 0);
console.log(`Wrote ${outputPath}`);
console.log(`Original ads: ${sets.length}`);
console.log(`Generated output folders scanned: ${generatedBySetId.size}`);
console.log(`Generated videos discovered: ${discoveredVariantCount}`);
console.log(`Generated videos matched: ${variantCount}`);

const originalsWithoutGenerated = sets.filter((set) => set.variants.length === 0);
const unmatchedGenerated = [...generatedBySetId.values()]
  .filter((folder) => !matchedGeneratedIds.has(folder.setId));

if (originalsWithoutGenerated.length) {
  console.warn(`Original ads without matched generated videos: ${originalsWithoutGenerated.length}`);
  for (const set of originalsWithoutGenerated.slice(0, 20)) {
    const canonicalId = canonicalSetId(set.id);
    const expected = canonicalId === set.id ? `"${set.id}"` : `"${canonicalId}" or "${set.id}"`;
    console.warn(`  - ${set.seed.fileName} -> expected folder id ${expected}`);
  }
}

if (unmatchedGenerated.length) {
  console.warn(`Generated folders not matched to an original ad: ${unmatchedGenerated.length}`);
  for (const folder of unmatchedGenerated.slice(0, 20)) {
    console.warn(`  - ${folder.title} -> folder id "${folder.setId}", videos ${folder.variants.length}`);
  }
}

if (debug) {
  console.log('Original ids:');
  for (const file of originalFiles) console.log(`  - ${slugId(stripExtension(file.name))} (${file.name})`);
  console.log('Generated folder ids:');
  for (const folder of generatedBySetId.values()) console.log(`  - ${folder.setId} (${folder.variants.length} videos)`);
}

function env(name) {
  return process.env[name]?.trim();
}

function parseArgs(argv) {
  const parsed = {};
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (!arg.startsWith('--')) continue;
    const key = arg.slice(2);
    const value = argv[index + 1];
    if (!value || value.startsWith('--')) {
      parsed[key] = 'true';
    } else {
      parsed[key] = value;
      index += 1;
    }
  }
  return parsed;
}

async function loadDotEnv(filePath) {
  try {
    const contents = await readFile(filePath, 'utf8');
    for (const line of contents.split(/\r?\n/)) {
      const trimmed = line.trim();
      if (!trimmed || trimmed.startsWith('#') || !trimmed.includes('=')) continue;
      const separator = trimmed.indexOf('=');
      const key = trimmed.slice(0, separator).trim();
      const rawValue = trimmed.slice(separator + 1).trim();
      if (!key || process.env[key] !== undefined) continue;
      process.env[key] = rawValue.replace(/^['"]|['"]$/g, '');
    }
  } catch (error) {
    if (error.code !== 'ENOENT') throw error;
  }
}

async function getAccessToken() {
  if (env('DROPBOX_ACCESS_TOKEN')) return env('DROPBOX_ACCESS_TOKEN');

  const refreshToken = env('DROPBOX_REFRESH_TOKEN');
  const appKey = env('DROPBOX_APP_KEY');
  const appSecret = env('DROPBOX_APP_SECRET');
  if (!refreshToken || !appKey || !appSecret) return '';

  const basicAuth = Buffer.from(`${appKey}:${appSecret}`).toString('base64');
  const response = await fetch('https://api.dropboxapi.com/oauth2/token', {
    method: 'POST',
    headers: {
      Authorization: `Basic ${basicAuth}`,
      'Content-Type': 'application/x-www-form-urlencoded'
    },
    body: new URLSearchParams({
      grant_type: 'refresh_token',
      refresh_token: refreshToken
    })
  });

  if (!response.ok) {
    const text = await response.text();
    fail(`Could not refresh Dropbox token: ${response.status} ${text}`);
  }

  const payload = await response.json();
  return payload.access_token;
}

function makeDropboxRoot(value) {
  if (/^https?:\/\//i.test(value)) {
    return { kind: 'shared_link', shareUrl: value, relativePath: '' };
  }
  return { kind: 'path', path: value };
}

function childDropboxRef(root, childName, entry) {
  if (root.kind === 'shared_link') {
    const relativePath = joinDropboxPath(root.relativePath || '', childName);
    return { kind: 'shared_link', shareUrl: root.shareUrl, relativePath };
  }
  return {
    kind: 'path',
    path: entry.path_lower || entry.path_display || joinDropboxPath(root.path, childName)
  };
}

async function listDropboxFolder(ref, options = {}) {
  const firstPage = await dropboxRpc('https://api.dropboxapi.com/2/files/list_folder', {
    path: ref.kind === 'shared_link' ? ref.relativePath || '' : ref.path,
    shared_link: ref.kind === 'shared_link' ? { url: ref.shareUrl } : undefined,
    recursive: ref.kind === 'shared_link' ? false : Boolean(options.recursive),
    include_deleted: false,
    include_non_downloadable_files: false
  });

  const entries = [...firstPage.entries];
  let cursor = firstPage.cursor;
  let hasMore = firstPage.has_more;

  while (hasMore) {
    const page = await dropboxRpc('https://api.dropboxapi.com/2/files/list_folder/continue', { cursor });
    entries.push(...page.entries);
    cursor = page.cursor;
    hasMore = page.has_more;
  }

  return entries;
}

async function discoverGeneratedFolders(root) {
  const folders = [];

  async function visit(folderRef, folderName) {
    const entries = await listDropboxFolder(folderRef);
    const hasGeneratedVideos = entries.some((entry) =>
      entry['.tag'] === 'file' && isGeneratedVideoFile(entry.name)
    );

    if (hasGeneratedVideos) {
      folders.push({
        name: folderName,
        ref: folderRef,
        entries
      });
    }

    const childFolders = entries
      .filter((entry) => entry['.tag'] === 'folder')
      .sort((left, right) => left.name.localeCompare(right.name));

    for (const child of childFolders) {
      await visit(childDropboxRef(folderRef, child.name, child), child.name);
    }
  }

  await visit(root, 'generated-outputs');
  return folders;
}

async function dropboxRpc(url, body) {
  const compactBody = Object.fromEntries(Object.entries(body).filter(([, value]) => value !== undefined));
  const response = await fetch(url, {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${accessToken}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify(compactBody)
  });

  if (!response.ok) {
    const text = await response.text();
    fail(`Dropbox API error at ${url}: ${response.status} ${text}`);
  }

  return response.json();
}

async function getOrCreateSharedLink(entry) {
  const filePath = entry.path_lower || entry.path_display || entry.id;
  const existing = await listSharedLinks(filePath);
  if (existing.length) return existing[0].url;

  const response = await fetch('https://api.dropboxapi.com/2/sharing/create_shared_link_with_settings', {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${accessToken}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify({ path: filePath, settings: { requested_visibility: 'public' } })
  });

  if (response.ok) {
    return (await response.json()).url;
  }

  const text = await response.text();
  if (text.includes('shared_link_already_exists')) {
    const links = await listSharedLinks(filePath);
    if (links.length) return links[0].url;
  }

  fail(`Could not create shared link for ${entry.name}: ${response.status} ${text}`);
}

async function listSharedLinks(filePath) {
  const payload = await dropboxRpc('https://api.dropboxapi.com/2/sharing/list_shared_links', {
    path: filePath,
    direct_only: true
  });
  return payload.links || [];
}

async function readKeywords(entries, folderRef) {
  const keywordFile = entries.find((entry) =>
    entry['.tag'] === 'file' &&
    ['final_keywords.csv', 'mining_summary.csv', 'merged_keywords.csv'].includes(entry.name)
  );
  if (!keywordFile) return [];

  try {
    const csv = await downloadSmallText(keywordFile, folderRef);
    return parseKeywords(csv);
  } catch (error) {
    console.warn(`Could not read keywords from ${keywordFile.name}: ${error.message}`);
    return [];
  }
}

async function downloadSmallText(entry, folderRef) {
  const filePath = entry.path_lower || entry.path_display || entry.id;
  const apiArg = { path: filePath };
  if (folderRef.kind === 'shared_link' && !entry.path_lower && !entry.path_display) {
    apiArg.path = joinDropboxPath(folderRef.relativePath || '', entry.name);
  }

  const response = await fetch('https://content.dropboxapi.com/2/files/download', {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${accessToken}`,
      'Dropbox-API-Arg': JSON.stringify(apiArg)
    }
  });

  if (!response.ok) {
    throw new Error(`${response.status} ${await response.text()}`);
  }

  return response.text();
}

function parseKeywords(csv) {
  const rows = parseCsvRows(csv)
    .map((row) => row.map(cleanCsvValue))
    .filter((row) => row.some(Boolean));

  if (!rows.length) return [];

  const header = rows[0].map(normalizeCsvHeader);
  const hasHeader = header.some((value) => CSV_HEADER_VALUES.has(value));
  const keywordColumns = hasHeader
    ? header.map((value, index) => KEYWORD_COLUMN_NAMES.has(value) ? index : -1).filter((index) => index >= 0)
    : [];
  const valueRows = hasHeader ? rows.slice(1) : rows;
  const values = keywordColumns.length
    ? valueRows.flatMap((row) => keywordColumns.map((index) => row[index] || ''))
    : valueRows.flat();

  return uniqueList(values
    .map(cleanCsvValue)
    .filter(isKeywordValue)
  ).slice(0, 8);
}

function parseCsvRows(csv) {
  const rows = [];
  let row = [];
  let value = '';
  let inQuotes = false;

  for (let index = 0; index < csv.length; index += 1) {
    const char = csv[index];
    const next = csv[index + 1];

    if (char === '"' && inQuotes && next === '"') {
      value += '"';
      index += 1;
    } else if (char === '"') {
      inQuotes = !inQuotes;
    } else if (char === ',' && !inQuotes) {
      row.push(value);
      value = '';
    } else if ((char === '\n' || char === '\r') && !inQuotes) {
      if (char === '\r' && next === '\n') index += 1;
      row.push(value);
      rows.push(row);
      row = [];
      value = '';
    } else {
      value += char;
    }
  }

  row.push(value);
  rows.push(row);
  return rows;
}

function cleanCsvValue(value) {
  return String(value || '')
    .trim()
    .replace(/^["']|["']$/g, '')
    .trim();
}

function normalizeCsvHeader(value) {
  return cleanCsvValue(value)
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '_')
    .replace(/^_+|_+$/g, '');
}

function isKeywordValue(value) {
  const cleaned = cleanCsvValue(value);
  if (cleaned.length < 3) return false;
  if (/^\d+(\.\d+)?$/.test(cleaned)) return false;
  if (CSV_HEADER_VALUES.has(normalizeCsvHeader(cleaned))) return false;
  return true;
}

function isVideoFile(fileName) {
  return /\.mp4$/i.test(fileName);
}

function isGeneratedVideoFile(fileName) {
  return isVideoFile(fileName) && /^new_ad_/i.test(fileName);
}

function stripExtension(fileName) {
  return fileName.replace(/\.[^.]+$/, '');
}

function modelKeyFromFilename(fileName) {
  return stripExtension(fileName)
    .replace(/^new_ad_/i, '')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '');
}

function modelSortKey(fileName) {
  const key = modelKeyFromFilename(fileName);
  const index = EXPECTED_MODEL_ORDER.indexOf(key);
  return index === -1 ? 99 : index;
}

function slugId(value) {
  return String(value)
    .toLowerCase()
    .replace(/&/g, ' and ')
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-+|-+$/g, '')
    .replace(/-{2,}/g, '-') || 'video';
}

function titleFromSlug(value) {
  return String(value)
    .replace(/[-_]+/g, ' ')
    .replace(/\b\w/g, (letter) => letter.toUpperCase())
    .replace(/\bSd15\b/g, 'SD 1.5')
    .replace(/\bKdd\b/g, 'KDD');
}

function keywordsFromTitle(title) {
  return uniqueList(slugId(title)
    .split('-')
    .filter((word) => word.length >= 3 && !STOPWORDS.has(word))
  ).slice(0, 6);
}

function uniqueList(values) {
  return [...new Set(values.map((value) => String(value).trim()).filter(Boolean))];
}

function findGeneratedMatch(originalId, generatedBySetId) {
  if (generatedBySetId.has(originalId)) return generatedBySetId.get(originalId);
  const canonicalOriginalId = canonicalSetId(originalId);
  if (generatedBySetId.has(canonicalOriginalId)) return generatedBySetId.get(canonicalOriginalId);

  const directMatches = [...generatedBySetId.entries()]
    .filter(([generatedId]) => {
      const canonicalGeneratedId = canonicalSetId(generatedId);
      return (
        canonicalGeneratedId.length >= 6 &&
        canonicalOriginalId.length >= 6 &&
        (canonicalGeneratedId.includes(canonicalOriginalId) || canonicalOriginalId.includes(canonicalGeneratedId))
      );
    });

  if (directMatches.length === 1) return directMatches[0][1];

  const fuzzyMatches = [...generatedBySetId.entries()]
    .map(([generatedId, generated]) => ({
      generated,
      score: matchScore(canonicalOriginalId, canonicalSetId(generatedId))
    }))
    .filter((candidate) => candidate.score.overlap >= 2 && candidate.score.coverage >= 0.6)
    .sort((left, right) =>
      right.score.weighted - left.score.weighted ||
      right.score.overlap - left.score.overlap
    );

  if (!fuzzyMatches.length) return undefined;

  const [best, next] = fuzzyMatches;
  if (next && Math.abs(best.score.weighted - next.score.weighted) < 0.05) return undefined;

  return best.generated;
}

function canonicalSetId(value) {
  return slugId(value).replace(/^(?:\d{1,4}-)+(?=[a-z])/i, '');
}

function matchScore(originalId, generatedId) {
  const originalTokens = new Set(matchTokens(originalId));
  const generatedTokens = matchTokens(generatedId);
  if (!originalTokens.size || !generatedTokens.length) {
    return { coverage: 0, jaccard: 0, overlap: 0, weighted: 0 };
  }

  const overlap = generatedTokens.filter((token) => originalTokens.has(token)).length;
  const union = new Set([...originalTokens, ...generatedTokens]).size;
  const coverage = overlap / generatedTokens.length;
  const jaccard = overlap / union;

  return {
    coverage,
    jaccard,
    overlap,
    weighted: (coverage * 0.75) + (jaccard * 0.25)
  };
}

function matchTokens(value) {
  return uniqueList(slugId(value)
    .split('-')
    .map(normalizeMatchToken)
    .filter((token) => token.length >= 2)
    .filter((token) => !/^\d+$/.test(token))
    .filter((token) => !STOPWORDS.has(token)));
}

function normalizeMatchToken(token) {
  if (token.length > 4 && token.endsWith('ies')) return `${token.slice(0, -3)}y`;
  if (token.length > 4 && token.endsWith('es')) return token.slice(0, -2);
  if (token.length > 4 && token.endsWith('s')) return token.slice(0, -1);
  return token;
}

function joinDropboxPath(parent, child) {
  const cleanParent = parent === '/' ? '' : parent.replace(/\/+$/g, '');
  const cleanChild = child.replace(/^\/+/g, '');
  return `${cleanParent}/${cleanChild}`;
}

function redactToken(value) {
  try {
    const url = new URL(value);
    if (url.searchParams.has('st')) url.searchParams.set('st', 'redacted');
    return url.toString();
  } catch {
    return value;
  }
}

function fail(message) {
  console.error(message);
  process.exit(1);
}
